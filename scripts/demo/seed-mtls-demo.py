# Verification
# =============================================================================


def verify_consumers(token: str | None) -> int:
    """Verify consumers are visible via the API. Returns count or -1 on error."""
    headers = {}
    if token:
        headers["Authorization"] = f"Bearer {token}"

    try:
        resp = httpx.get(
            f"{API_URL}/v1/consumers/{MTLS_TENANT}",
            headers=headers,
            params={"page_size": 1},
            timeout=10.0,
        )
        if resp.status_code == 200:
            return resp.json().get("total", 0)
        return -1
    except Exception:
        return -1


def verify_fingerprints() -> int:
    """Verify fingerprints.csv has expected entries. Returns count."""
    fp_path = CERTS_DIR / "fingerprints.csv"
    if not fp_path.exists():
        return 0
    lines = fp_path.read_text().strip().splitlines()
    return max(0, len(lines) - 1)  # minus header


def print_verification_summary(
    token: str | None,
    total: int,
    success: int,
    failed: int,
    credentials: list[dict],
) -> None:
    """Print verification results and next steps (pattern from seed-demo-data.py)."""
    print()
    print("=" * 60)
    print("  VERIFICATION")
    print("=" * 60)

    # Check API visibility
    api_count = verify_consumers(token)
    fp_count = verify_fingerprints()
    creds_count = len(credentials)

    checks = [
        ("Bulk upload", success > 0, f"{success}/{total} consumers created"),
        ("Credentials saved", creds_count > 0, f"{creds_count} in credentials.json"),
        ("Fingerprints CSV", fp_count > 0, f"{fp_count} entries"),
        (
            "API visibility",
            api_count > 0,
            f"{api_count} consumers via GET /v1/consumers/{MTLS_TENANT}"
            if api_count >= 0
            else "API not reachable (offline mode?)",
        ),
    ]

    all_ok = True
    for name, ok, detail in checks:
        status = "[OK]" if ok else "[--]"
        if not ok:
            all_ok = False
        print(f"  {status} {name}: {detail}")

    # Demo credentials (first 3)
    if credentials:
        print()
        print("  Demo Credentials (first 3):")
        print("  " + "-" * 56)
        for c in credentials[:3]:
            print(f"    {c['external_id']}")
            print(f"      client_id:     {c.get('client_id', 'N/A')}")
            print(f"      client_secret:  {c.get('client_secret', 'N/A')[:20]}...")

    # Verification commands
    print()
    print("  Verify manually:")
    print("  " + "-" * 56)
    print(f"    # List consumers (API)")
    print(f"    curl -s {API_URL}/v1/consumers/{MTLS_TENANT}?page_size=5 \\")
    print(f"      -H \"Authorization: Bearer $ADMIN_TOKEN\" | python3 -m json.tool")
    print()
    print(f"    # Check fingerprints")
    print(f"    head -5 {CERTS_DIR}/fingerprints.csv")
    print()
    print(f"    # Verify cert chain")
    print(f"    openssl verify -CAfile {CERTS_DIR}/stoa-demo-ca.pem {CERTS_DIR}/client-001.pem")

    # Next steps
    print()
    print("  Next steps:")
    print("  " + "-" * 56)
    print("    1. source scripts/demo/mtls-demo-commands.sh   # Load demo variables")
    print("    2. Run STEP 1-6 during the demo                # Copy-paste curl commands")
    print()
    print("  Console UI:")
    print("  " + "-" * 56)
    print(f"    NOTE: No consumer list page in Console UI yet.")
    print(f"    Consumers are visible via API only (curl or Postman).")
    print(f"    The Portal has CertificateUploader component (not wired to listing).")

    print()
    print("=" * 60)
    if all_ok:
        print(f"  READY: {success} consumers with mTLS certificates")
    elif success > 0:
        print(f"  PARTIAL: {success} ok, {failed} failed — check logs above")
    else:
        print(f"  FAILED: No consumers created — check API connectivity")
    print("=" * 60)


# =============================================================================
# Main
# =============================================================================


def main() -> None:
    parser = argparse.ArgumentParser(description="STOA mTLS Demo Seed (CAB-864)")
    parser.add_argument("--force", action="store_true", help="Overwrite existing credentials")
    parser.add_argument(
        "--profile",
        choices=["default", "mixed"],
        default="default",
        help="Seed profile: 'mixed' revokes consumers 099-100 for demo edge cases",
    )
    args = parser.parse_args()

    creds_path = CERTS_DIR / "credentials.json"

    print("=" * 60)
    print("  STOA mTLS Demo Seed (CAB-864)")
    print("=" * 60)
    print(f"  API:     {API_URL}")
    print(f"  KC:      {KEYCLOAK_URL}")
    print(f"  Tenant:  {MTLS_TENANT}")
    print(f"  Certs:   {CERTS_DIR}")
    print(f"  Profile: {args.profile}")
    print()

    # Check certs exist
    if not CERTS_DIR.exists():
        print(f"  [-] Certs directory not found: {CERTS_DIR}")
        print("      Run: ./scripts/demo/generate-mtls-certs.sh")
        sys.exit(1)

    # Check if already seeded
    if creds_path.exists() and not args.force:
        data = json.loads(creds_path.read_text())
        creds = data.get("credentials", data) if isinstance(data, dict) else data
        count = len(creds)
        print(f"  [!] Already seeded ({count} credentials in {creds_path})")
        print("      Use --force to re-seed")
        sys.exit(0)

    # Get auth token
    print("[1/4] Authenticating...")
    token = get_admin_token()
    if token:
        print("  [+] Admin token acquired")
    else:
        print("  [!] Proceeding without auth (may fail on protected endpoints)")

    # Bulk onboard
    print("[2/4] Bulk onboarding consumers with certificates...")
    result = seed_consumers(token)

    total = result.get("total", 0)
    success = result.get("success", 0)
    failed = result.get("failed", 0)

    print(f"\n  Result: {success}/{total} consumers created ({failed} failed)")

    # Mixed profile post-processing (revoke 2 consumers)
    if args.profile == "mixed" and success > 0:
        print("[2b/4] Applying mixed profile (revoking 2 consumers)...")
        result = apply_mixed_profile(token, result)

    # Save credentials
    print("[3/4] Saving credentials...")
    save_credentials(result)

    # Load saved credentials for verification summary
    creds_path_final = CERTS_DIR / "credentials.json"
    saved_creds = []
    if creds_path_final.exists():
        data = json.loads(creds_path_final.read_text())
        saved_creds = data.get("credentials", data) if isinstance(data, dict) else data

    # Verification + next steps
    print("[4/4] Verifying...")
    print_verification_summary(token, total, success, failed, saved_creds)


if __name__ == "__main__":
    main()
