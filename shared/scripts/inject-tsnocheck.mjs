#!/usr/bin/env node
// Prepend a `// @ts-nocheck` header to a generated.ts file produced by
// openapi-typescript. Required because the upstream OpenAPI snapshot
// contains duplicate operationId entries that cause TS2300/TS2717 errors
// in `interface operations` (cf. control-plane-ui/REWRITE-BUGS.md BUG-3).
//
// Usage: node scripts/inject-tsnocheck.mjs <file.ts>
//
// Idempotent: detects an already-present `// @ts-nocheck` and skips. So this
// script can be safely re-run on the same file or on the CI temp file used
// by `check:api-types` (the diff stays meaningful).

import { readFileSync, writeFileSync } from 'node:fs';
import { argv, exit } from 'node:process';

const file = argv[2];
if (!file) {
  console.error('Usage: inject-tsnocheck.mjs <file>');
  exit(2);
}

const HEADER = `// @ts-nocheck
/**
 * This file was auto-generated by openapi-typescript.
 * Do not make direct changes to the file.
 *
 * \`@ts-nocheck\` is required because the upstream OpenAPI snapshot
 * (control-plane-api/openapi-snapshot.json) contains duplicate operationId
 * entries (e.g. list_applications, create_budget, list_deployments) that
 * cause TS2300/TS2717 errors in \`interface operations\`.
 *
 * This is a backend contract bug (BUG-3 in control-plane-ui/REWRITE-BUGS.md).
 * Type IMPORTS still resolve correctly because TypeScript continues to parse
 * declarations — only error-reporting on this file is suppressed.
 *
 * Regen: \`cd shared && npm run generate:api-types\` (this header is
 * re-injected automatically by scripts/inject-tsnocheck.mjs — do not
 * delete it manually).
 */
`;

const content = readFileSync(file, 'utf8');
if (content.startsWith('// @ts-nocheck')) {
  // Already injected — idempotent no-op.
  exit(0);
}

// openapi-typescript writes a small header comment of its own; strip it
// so we don't end up with two stacked headers.
const STOCK_HEADER = /^\/\*\*\s*\n[\s\S]*?\*\/\s*\n+/;
const stripped = content.replace(STOCK_HEADER, '');

writeFileSync(file, HEADER + stripped);
